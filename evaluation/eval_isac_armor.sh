#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "${SCRIPT_DIR}"

export CUDA_VISIBLE_DEVICES="${CUDA_VISIBLE_DEVICES:-0}"

GEN_MODEL="${GEN_MODEL:-meta-llama/Meta-Llama-3-8B-Instruct}"
EMB_MODEL="${EMB_MODEL:-intfloat/e5-large-v2}"
JUDGE_MODEL="${JUDGE_MODEL:-gpt-5.2}"

# Paths produced/used by the ARMOR training repo.
ARMOR_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
CKPT_ROOT="${CKPT_ROOT:-$ARMOR_ROOT/retriever_training/checkpoints}"
UNIFIED_INDEX="${UNIFIED_INDEX:-$ARMOR_ROOT/data_gen/data/unified/unified_index.faiss}"
UNIFIED_DB="${UNIFIED_DB:-$ARMOR_ROOT/data_gen/data/unified/unified_chunks.sqlite}"

# Test data generated by data_gen pipeline
TEST_DATA="${TEST_DATA:-$ARMOR_ROOT/data_gen/data/isac/splits/raft/test.jsonl}"
# QnA data for out-of-corpus evaluation
TELE_QNA_DATA="${TELE_QNA_DATA:-$ARMOR_ROOT/evaluation/tele-qna/data/isac_100.jsonl}"

OUT_ROOT="${OUT_ROOT:-$ARMOR_ROOT/evaluation/results_isac}"
TELE_EVAL_PY="./tele-eval/eval_tele_eval.py"
TELE_QNA_PY="./tele-qna/eval_tele_qna.py"

# Hyperparameters matching the training output path
LR="${LR:-2e-5}"
BS="${BS:-4}"
TOP_K="${TOP_K:-16}"
TEMP_LR="${TEMP_LR:-1e-2}"
QUERY_DISTILL="${QUERY_DISTILL:-1.0}"
INIT_CONTR_TEMP="${INIT_CONTR_TEMP:-10.0}"
INIT_RETR_TEMP="${INIT_RETR_TEMP:-10.0}"
MIN_TEMP="${MIN_TEMP:-1.0}"

ARMOR_QE="$CKPT_ROOT/$GEN_MODEL/isac_unified_corpus_armor-lr=$LR-temp_lr=$TEMP_LR-batch_size=$BS-rag_top_k=$TOP_K-rag_lambd=1.0-contr_lambd=1.0-qdistill=$QUERY_DISTILL-init_contrastive_temperature=$INIT_CONTR_TEMP-init_retr_temperature=$INIT_RETR_TEMP-min_temp=$MIN_TEMP/query_encoder_final"

run_tele_eval() {
  local method="$1"
  local mode="$2"
  local query_encoder="${3:-}"

  local out_dir="$OUT_ROOT/tele_eval/$method"
  echo "=== Running Tele-Eval for $method ==="
  
  local args=(
    "$TELE_EVAL_PY"
    --model "$GEN_MODEL"
    --data-path "$TEST_DATA"
    --output-dir "$out_dir"
    --retrieval-mode "$mode"
    --dtype bfloat16
    --device cuda
    --judge-model "$JUDGE_MODEL"
    --retrieval-top-k 16
    --retrieval-max-len 384
    --max-context-chars 12000
    --max-new-tokens 128
  )

  if [[ "$mode" != "closed_book" ]]; then
    args+=(
      --faiss-index "$UNIFIED_INDEX"
      --sqlite-db "$UNIFIED_DB"
      --embed-model "$EMB_MODEL"
      --query-encoder-path "$query_encoder"
    )
  fi

  python "${args[@]}"
}

run_tele_qna() {
  local method="$1"
  local mode="$2"
  local query_encoder="${3:-}"

  local out_dir="$OUT_ROOT/tele_qna/$method"
  echo "=== Running Tele-QnA for $method ==="
  
  local args=(
    "$TELE_QNA_PY"
    --model "$GEN_MODEL"
    --data-path "$TELE_QNA_DATA"
    --output-dir "$out_dir"
    --retrieval-mode "$mode"
    --dtype bfloat16
    --device cuda
    --max-new-tokens 32
    --limit 50
  )

  if [[ "$mode" != "closed_book" ]]; then
    args+=(
      --faiss-index "$UNIFIED_INDEX"
      --sqlite-db "$UNIFIED_DB"
      --embed-model "$EMB_MODEL"
      --query-encoder-path "$query_encoder"
      --retrieval-top-k 16
      --retrieval-max-len 384
      --max-context-chars 12000
    )
  fi

  python "${args[@]}"
}

# 1. Closed Book Baseline
run_tele_eval "base_closed_book" "closed_book"
run_tele_qna "base_closed_book" "closed_book"

# 2. Base RAG Baseline
run_tele_eval "base_rag" "rag" "$EMB_MODEL"
run_tele_qna "base_rag" "rag" "$EMB_MODEL"

# 3. ARMOR (Adaptive Regularized Mixture Optimization)
if [[ -d "$ARMOR_QE" ]]; then
  run_tele_eval "armor" "rag" "$ARMOR_QE"
  run_tele_qna "armor" "rag" "$ARMOR_QE"
else
  echo "Warning: ARMOR query encoder checkpoint not found at: $ARMOR_QE"
  echo "Please run training first using: ../retriever_training/train_isac_armor.sh"
fi
